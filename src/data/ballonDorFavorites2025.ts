export const initializeBallonDorPlayers = async () => {
  try {
    const { supabase } = await import('@/lib/supabaseClient');
    
    if (supabase) {
      // Vérifier si les joueurs existent déjà
      const { data: existingPlayers } = await supabase
        .from('players')
        .select('id')
        .limit(1);
      
      // Si aucun joueur n'existe, les insérer
      if (!existingPlayers || existingPlayers.length === 0) {
        const { error } = await supabase
          .from('players')
          .insert(ballonDorFavorites2025);
        
        if (error) {
          console.error('Error inserting players:', error);
        } else {
          console.log('✅ Ballon d\'Or 2025 players initialized successfully');
        }
      }
    }
  } catch (error) {
    console.error('Error initializing players:', error);
  }
};